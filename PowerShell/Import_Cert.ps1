[console]::ForegroundColor = "DarkCyan"
Start-Sleep -Seconds 1
Write-host #
Write-host "--------------------------------------------------"
Write-host "!!!*Starting Import Certificate Script*!!!" -ForegroundColor Magenta
Write-host "--------------------------------------------------"
Write-host #

Set-Secret -Name PFXPassword -Secret (ConvertTo-SecureString "pass" -AsPlainText -Force) -Vault GitVault

# Retrieve the PFX password from the secret vault
$pfxPassword = Get-Secret -Name PFXPassword -Vault GitVault

# Define the certificate path
$pfxPath = "C:\temp\Git\CodeSigning.pfx"

# Import the PFX certificate into the Personal store
$certificate = New-Object System.Security.Cryptography.X509Certificates.X509Certificate2
$certificate.Import($pfxPath, $pfxPassword, "Exportable,PersistKeySet")

$personalStore = New-Object System.Security.Cryptography.X509Certificates.X509Store("My", "CurrentUser")
$personalStore.Open("ReadWrite")
$personalStore.Add($certificate)
$personalStore.Close()

# Import the PFX certificate into the Trusted Root Certification Authorities store
$rootStore = New-Object System.Security.Cryptography.X509Certificates.X509Store("Root", "CurrentUser")
$rootStore.Open("ReadWrite")
$rootStore.Add($certificate)
$rootStore.Close()

Write-Host "PFX certificate imported successfully using GitVault!" -ForegroundColor Green
Exit
